		<strong>Brief History of North Luzon Philippines State College</strong></p>
		<strong>NLPSC Main Campus</strong>
		<br><br> The metamorphosis of North Luzon Philippines State College (NLPSC) as a public higher education institution in the Second District of Ilocos Sur can be traced in its humble, yet glorious history.
		<br><br> The institution evolved in 1975 from a small community college known as Candon Community College (CCC) which offered non-degree courses, the midwifery and the secretarial course.
		<br><br> Through a bill sponsored by then-Congressman, Hon. Eric D. Singson, Candon Community College sought integration to the University of Northern Philippines in Vigan, Ilocos Sur; hence,
		Republic Act 6744 effected the establishment of CCC-University of Northern Philippines Branch on August 11, 1989.
		<br><br> As CCC-UNP, it strived to provide quality education offering additional courses such as Bachelor of Arts (AB), Bachelor in Elementary Education (BEEd), Bachelor in Secondary Education (BSE),
		Bachelor of Science in Business Administration (BSBA), Bachelor of Science in Computer Science (BSCS), and Bachelor of Science in Criminology (BSCrim) aside from the pioneer non-degree courses in
		midwifery and secretarial. The secretarial course has been enhanced into two new certificate programs: the Associate in Office Management (AOM) and the Associate in Medical Office Management (AMOM).
		<br><br> Realizing the vision of Hon. Eric D. Singson for Candon to have a college of its own during his long years as dynamic political leader in the municipality, he sponsored a bill that amended R.A.
		6744 and helped establish a new college in the province. Through Republic Act 10085, otherwise known as “An Act Separating the CCC-University of University of Northern Philippines Branch from the University
		of Northern Philippines in the City of Vigan, both located in the province of Ilocos Sur, converting it into a State College to be known as North Luzon Philippines State College and Appropriating Funds Thereof”,
		North Luzon Philippines State College started to operate as a public higher education institution on May 5, 2010.
		<br><br> In pursuance to Republic Act 7722, otherwise known as the “Higher Education Act of 1994” and RA 10085, Acting Director IV of the Commission on Higher Education Regional Office, Dr. Caridad O. Abuan,
		was designated as Officer-in-Charge of the College on August 11, 2010 by CHED chairperson, Dr. Patricia B. Licuanan. Dr. Abuan ensured a smooth transition of operation and management of the college from the
		University of Northern Philippines. Formally in 2010, NLPSC set itself to work. A new course, the Bachelor of Science in Office Administration (BSOA) was offered in the Business Education Department.
		<br><br> On August 16, 2012, CHED Chairperson, Dr. Patricia B. Licuanan designated Dr. Elizabeth M. Gacusana as the new Officer-in-Charge of the College.
		<br><br> With regards to its curricular mandate, NLPSC has consistently proven its adherence to quality education.
		<br><br> In August 2012, the first 25 Licensure Examination for Teachers (LET) takers from the BEEd Department garnered a 100% institutional passing rate. This is the first time in the history of the
		institution that a program got a sterling one hundred percent. The national passing rate was 49.29%. Succeeding board examinations showed NLPSC’s excellent performance by obtaining an institutional passing
		rate higher than the national passing rate. Other board programs such as the BSE, Midwifery and BS Criminology also showed satisfactory performance in the board examinations.
		<br><br> In August 2014 LET, 52 out of 61 examinees (85.25%) passed the exam for BEEd and NLPSC set its record in its history as the Top Ten Performing School (tenth place) nationwide besting the 1,313 Higher
		Education Institutions in the country. The following year (2015), BEEd LET takers duplicated the achievement when 53 out of 63 takers (84.13%) also passed the said board examination. NLPSC was then awarded
		for the second time as the Top Ten Performing School in the entire country.
		<br><br> On September 19, 2015, NLPSC had its first SUC President in the person of Dr. Elizabeth M. Gacusana. She has set various innovative moves for the College to respond to the demands of time through
		her SIRIG-BETH institutional and academic agenda for NLPSC. She strategized and envisioned a culture of excellence and dynamism in all endeavours and service-oriented programs and activities for the college
		during her term until 2019.
